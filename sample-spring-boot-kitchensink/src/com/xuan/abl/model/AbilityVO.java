package com.xuan.abl.model;

public class AbilityVO implements java.io.Serializable{
	private String ability_no, ability_name;

	public AbilityVO() {
		super();
	}

	public String getAbility_no() {
		return ability_no;
	}

	public void setAbility_no(String ability_no) {
		this.ability_no = ability_no;
	}

	public String getAbility_name() {
		return ability_name;
	}

	public void setAbility_name(String ability_name) {
		this.ability_name = ability_name;
	}

	
}
